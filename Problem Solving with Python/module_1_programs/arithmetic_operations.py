#Write a program to enter 2 integers and perform all arithmetic operations on them

int1 = int(input("Enter first number: "))
int2 = int(input("Enter second number: "))

print("\nSum =", int1 + int2)
print("Difference =", int1 - int2)
print("Product =", int1 * int2)
print("Quotient =", int1 // int2)
print("Remainder =", int1 % int2)
print("Pewer =", int1 ** int2, "\n")